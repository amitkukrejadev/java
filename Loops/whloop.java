public class whloop {
    public static void main(String[] args) {
        int i = 0;
        while (i < 10) {
            System.out.println(i);
            i++; // Theek se increment karo
        }
        int a = 2;
        do {
            System.out.println(a);
            a = a + 2;
        } while (a < 20);
}
}
